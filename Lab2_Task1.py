# Crear variable string con valor "123"
my_string = "123"
print(f"Variable original: {my_string} (tipo: {type(my_string)})")
print("\n--- Conversiones con '123' ---")

# Conversión a integer
to_int = int(my_string)
print(f"Integer: {to_int} (tipo: {type(to_int)})")

# Conversión a float
to_float = float(my_string)
print(f"Float: {to_float} (tipo: {type(to_float)})")

# Conversión a boolean
to_bool = bool(my_string)
print(f"Boolean: {to_bool} (tipo: {type(to_bool)})")

# Conversión a list
to_list = list(my_string)
print(f"✓ List: {to_list} (tipo: {type(to_list)})")

# Conversión a tuple
to_tuple = tuple(my_string)
print(f"Tuple: {to_tuple} (tipo: {type(to_tuple)})")

