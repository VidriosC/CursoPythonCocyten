# Definir un número entero y un flotante
num_int = 10
num_float = 3.5

print("=== OPERACIONES CON INT Y FLOAT ===")
print(f"Número entero: {num_int} (tipo: {type(num_int)})")
print(f"Número flotante: {num_float} (tipo: {type(num_float)})")

# Suma
resultado_suma = num_int + num_float
print(f"SUMA: {num_int} + {num_float} = {resultado_suma}")
print(f"Tipo de dato del resultado: {type(resultado_suma)}")

# Resta
resultado_resta = num_int - num_float
print(f"RESTA: {num_int} - {num_float} = {resultado_resta}")
print(f"Tipo de dato del resultado: {type(resultado_resta)}")

# Multiplicación
resultado_mult = num_int * num_float
print(f"MULTIPLICACIÓN: {num_int} × {num_float} = {resultado_mult}")
print(f"Tipo de dato del resultado: {type(resultado_mult)}")

# División
resultado_div = num_int / num_float
print(f"DIVISIÓN: {num_int} ÷ {num_float} = {resultado_div}")
print(f"Tipo de dato del resultado: {type(resultado_div)}")
